#include "3-calc.h"
/**
 * op_add - addition
 * @a: input numb1
 * @b: input numb2
 * Return: addition result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: input numb1
 * @b: input numb2
 * Return: sub result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mutiply
 * @a: input numb1
 * @b: input numb2
 * Return: multiplication result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: input numb1
 * @b: input numb2
 * Return: division result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: input numb1
 * @b: input numb2
 * Return: modulo result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
